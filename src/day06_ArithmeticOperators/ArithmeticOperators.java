package day06_ArithmeticOperators;

public class ArithmeticOperators {

    public static void main(String[] args) {

        System.out.println( 10/4 ); //2
        System.out.println("10/4");  // text

        System.out.println(12 + 3.0); // 15.0

        System.out.println("12" + 3); // concatenation  123

        System.out.println( 'a' + 3 ); // addition, every single char has a corresponding number
        //                  97  + 3

        System.out.println( 12.0 - 4 ); // 8.0


       double a =   12.0 / 3 ;
        System.out.println(a);

        int b = 10/3;
        System.out.println(b);

        double c = 10/3;
        //      c =  3
        //      3.0

        System.out.println(c);


        double d = 10/3.0 ;
        //     d=  3.333...

        System.out.println(d);

        int x =   3/2;
        //  x =   1

        System.out.println(x);

        double y =   3/2;
        //     y  = 1
        //   1.0

        System.out.println(y);

        double z = 3.0/2;

        System.out.println(z);

        System.out.println("==============================================");

        System.out.println(100%13);
        System.out.println(100%10);

        System.out.println( 20%4.5);






    }

}
